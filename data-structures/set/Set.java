/*	** Set Interface:

	The set interface is part of the Java Collections Framework and is used
to store a collection of unique elements.

	Unlike a List, a Set does not allow duplicates, and it does not preserve
the order of the elements (unless you're using TreeSet or LinkedHashSet).

	Common classes that implement set:

		- HashSet 	- fast and unordered.

		- TreeSet 	- sorted set.

		-LinkedHashSet 	- ordered by insertion.
 
	* Use a set when you need to store unique value only.
	

	** Common Set Methods:

		- add()

		- remove()

		- contains()

		- size()

		- clear()



	** Set vs. List

	List:				Set:

	Allows duplicates.		Does not allow duplicates.
	
	Maintains order.		Does not guarantee order.

	Access by index.		No index-based access.


 * */
